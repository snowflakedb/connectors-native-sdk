CREATE OR REPLACE VIEW PUBLIC.INGESTION_DEFINITIONS AS
SELECT ID,
       RESOURCE_ID,
       NAME,
       ENABLED AS IS_ENABLED
FROM STATE.RESOURCE_INGESTION_DEFINITION;

GRANT SELECT ON VIEW PUBLIC.INGESTION_DEFINITIONS TO APPLICATION ROLE ADMIN;
GRANT SELECT ON VIEW PUBLIC.INGESTION_DEFINITIONS TO APPLICATION ROLE VIEWER;
